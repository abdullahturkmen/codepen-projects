let whirlpool = document.querySelector(".whirlpool");

const listCards = () => {
    whirlpool.innerHTML = "";
    let rotateY = 0;
    let translateY = 0;
    let scale = 1;
    let transition;
    cards.map((e, index) => {
        rotateY = Math.floor(Math.random() * 360);
        translateY = Math.floor(Math.random() * 5000);
        scale = Math.random() + 0.4;
        transition = Math.random() * 10 + 100;
        cards[index].rotateY = rotateY;
        cards[index].translateY = translateY;
        cards[index].scale = scale;
        whirlpool.innerHTML += `<div style="transform: rotateX(90deg) rotateY(${rotateY}deg) rotateZ(-5deg) translateZ(270px) translateY(-${translateY}px) scale(${scale}); transition: all ${transition}s linear">${
            e.card
        }</div>`;
    });

    setTimeout(() => {
        for (let i = 0; i < whirlpool.children.length; i++) {
            rotateY = cards[i].rotateY + Math.floor(Math.random() * 360);
            translateY = cards[i].translateY + Math.floor(Math.random() * 2000);
            scale = cards[i].scale + Math.random() + 1;
            whirlpool.children[i].style.transform = `rotateX(90deg) rotateY(${rotateY}deg) rotateZ(-5deg) translateZ(270px) translateY(${translateY}px) scale(${scale})`;
        }
    }, 500);
};

listCards();

const reStart = () => {
    for (let i = 0; i < cards.length; i++) {
        cards[i].rotateY = 0;
        cards[i].translateY = 0;
        cards[i].scale = 1;
    }

    listCards();
};
